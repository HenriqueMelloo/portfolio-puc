// Exercício 03: Encontrar o maior valor em uma array

// Definindo o array com os valores
const numbers = [10, 20, 30, 40, 50];

// Utilizando o método Math.mas() com spread operator (...) para encontrar o maior valor no array
const max = Math.max(...numbers);

// Exibindo o maior valor encontrado
console.log("O maior número é:", max);